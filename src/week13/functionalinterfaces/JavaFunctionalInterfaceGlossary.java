package week13.functionalinterfaces;

public class JavaFunctionalInterfaceGlossary {

    /** CORE INTERFACES (9)
     * Consumer          -> takes 1 parameter, doesn't return anything
     * BiConsumer        -> takes 2 parameters, doesn't return anything
     * Supplier          -> doesn't take parameters, returns a value
     * Function          -> takes 1 parameter, returns a value
     * BiFunction        -> takes 2 parameters, returns a value
     * Predicate         -> takes 1 parameter, returns boolean
     * BiPredicate       -> takes 2 parameters, returns boolean
     * UnaryOperator     -> takes 1 parameter, returns a value of the same type
     * BinaryOperator    -> takes 2 parameters(of the same type), returns a value of the same type
     */
}
